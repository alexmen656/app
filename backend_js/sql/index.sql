CREATE TABLE products (
    barcode TEXT PRIMARY KEY,
    name TEXT,
    brand TEXT,
    image TEXT,
    ingredients TEXT,
    package_size TEXT,
    serving_quantity NUMERIC,
    serving_unit TEXT,
    calories NUMERIC,
    protein NUMERIC,
    carbs NUMERIC,
    fat NUMERIC,
    fiber NUMERIC,
    sugar NUMERIC,
    salt NUMERIC,
    sodium NUMERIC,
    saturated_fat NUMERIC
);
